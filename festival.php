	<h3>Le festival improbable et intemporel</h3>
	<h4>Un rendez-vous incontournable</h4>
		<p>
			Le <strong>Festival improbable et intemporel</strong> s’inscrit parmi les rendez-vous incontournables de l'Alsace. Durant 3 jours, des têtes d’affiche 
			ainsi que des nouveaux talents issus de la scène francophone et internationnale se succèderont sur 2 scènes dans une enceinte comprenant 
			bars et stands de nourriture. Une ambiance unique à Mulhouse !<br /><br />
		</p>
		<p>
			Le <strong>Festival improbable et intemporel</strong> bénéficie d’une réputation affirmée dans le monde, tant par son histoire, son ambiance, 
			que par la qualité de sa programmation. Un festival chéri par les artistes qui apprécient particulièrement la proximité avec le public. 
			Vivez un rendez-vous unique et original durant ces quelques jours, où Mulhouse vibrera au rythme de la musique !
		</p>
	<h4>Les débuts</h4>
		<p>
			Le <strong>Festival improbable et intemporel</strong> a vu le jour au début du 20ème siècle dans le petit village de Niederbrück dans la vallée de Masevaux. 
			L'idée de ce festival improbable et intemporel est né un 1er juin alors que Jules Rivière écoutait les oiseaux chanter dans son jardin.
		</p>
	<h4>Le site</h4>
		<p>
			C'est dans un écrin de verdure qu'a lieu le festival. Entre le Rhin et la fôret verte, le site est absolument remarquable et propose de nombreux espaces...
		</p>
	<h5>De nombreuses animations</h5>
		<p>
			Tout au long de ces 3 jours de festival, vous pourrez voir de nombreux spectacles et animations :
		</p>  
        <ul>
            <li>pièces de théâtre</li>
            <li>musique expérimentale</li>
            <li>cinéma en plein air</li>
            <li>bricolages</li>
            <li>cuisine d'herbes sauvages...</li>
        </ul>
	<h5>Un camping</h5>
		<p>
			Pour ceux qui viennent de loin, il est possible de dormir au camping du Festival à 10 minutes du lieu du Festival. Des navettes gratuites circuleront 
			toute la nuit.
		</p>
